function entrySpecsOfNode(graph: Graph, node: Node): string[] {
  // An npm-aliased edge uses alias identity for its descriptor and keys it under
  // the ALIAS, not the node's actual name (`<alias>@npm:<target>@<range>`); the
  // range already carries the `npm:<target>@…` locator. A canonical edge keys
  // under the node's own name. Mirrors the berry `entryKeyOfNode` alias branch.
  const liveSpecs = Array.from(graph.in(node.id))
    .filter(edge => edge.kind === 'dep' || edge.kind === 'optional')
    .filter(edge => edge.attrs?.range !== undefined)
    // A `workspace:` edge range (workspace:* / workspace:^) is NOT a yarn-classic
    // entry-key descriptor — yarn 1 has no workspace protocol; a member keys by its
    // own `file:` resolution + version. Emitting `<name>@workspace:*` as a key
    // reparses with MISSING_ENTRY/RESOLUTION_PIN diagnostics (breaking round-trip).
    .filter(edge => edge.attrs?.workspace !== true)
    .map(edge => {
      // A completed/mutated edge governed by an override carries the pin as
      // `overrideRange`; key by it so yarn's collapse dedups (`foo@^1` + `foo@1.0` →
      // the pin), not the raw declared range (else `--immutable` YN0028). But that
      // collapse only round-trips when the resolved version SATISFIES the declared
      // range — then the reparse semver-matches the declared descriptor to the pinned
      // entry. When the override takes the node OUT of range (resolutions REPLACING,
      // not narrowing), keep the declared descriptor as the key, else the consumer's
      // `<name> "<declared>"` finds no matching entry and dangles (CASE-B). The entry
      // version is the pin either way. Non-aliased only.
      const declared = edge.attrs!.range!
      const range = edge.attrs!.alias !== undefined
        ? declared
        : (edge.attrs!.overrideRange !== undefined && satisfiesSafe(node.version, declared)
            ? edge.attrs!.overrideRange
            : declared)
      return `${edge.attrs!.alias ?? node.name}@${range}`
    })

  // Union live-edge descriptors with verbatim orphan ranges from the
  // parse-time entry-key descriptor SIDECAR, NOT live-or-sidecar. yarn 1 keys an
  // entry by the full set of consumer descriptors that share its resolution, but
  // a `dependencies:` block only re-creates an edge for an IN-LOCK consumer — a
  // descriptor whose consumer's package.json is NOT in the lock (a manifest-blind
  // workspace in a monorepo, or a peer/devDep declared only in a root manifest)
  // leaves NO incoming edge on parse-without-manifests, so it ORPHANS. Emitting
  // live-only would drop that descriptor, leaving its consumer unable to find
  // the locked entry and forcing re-resolution.
  //
  // The sidecar is captured verbatim at parse (the full on-disk entry-key set,
  // per NodeId) and is classic's emit ANCHOR — the analogue of the berry
  // `entryKeyOfNode` `primary` spec, which always anchors the key and is only
  // ever ADDED to by live in-edges, never subtracted. Unioning preserves every
  // on-disk descriptor while LIVE reconstruction still governs MUTATED edges: a
  // post-parse `addEdge` with a new range surfaces via `liveSpecs` (and joins the
  // union); a removed node drops its whole sidecar entry via `pruneSidecar`
  // (optimize). A descriptor in the entry KEY creates only a `specIndex` lookup
  // on reparse — never an edge — so re-emitting a verbatim orphan descriptor
  // cannot resurrect a removed dependency edge.
  const sidecarSpecs = sidecarByGraph.get(graph)?.entrySpecs.get(node.id) ?? []
  const union = new Set<string>(liveSpecs)
  for (const spec of sidecarSpecs) union.add(spec)
  if (union.size > 0) {
    return Array.from(union).sort(cmpUtf16)
  }

  return [`${node.name}@${node.version}`]
}

function escapeQuoted(value: string): string {
  return value.replace(/\\/g, '\\\\').replace(/"/g, '\\"')
}

function collectDependencyBlockEntries(
  graph: Graph,
  srcId: string,
  emitDiagnostic: (d: Diagnostic) => void,
): Array<[string, string]> {
  return collectBlockEntries(
    graph,
    Array.from(graph.out(srcId, 'dep')).concat(graph.out(srcId, 'dev')),
    emitDiagnostic,
  )
}

function collectBlockEntries(
  graph: Graph,
  edges: readonly Edge[],
  emitDiagnostic: (d: Diagnostic) => void,
): Array<[string, string]> {
  return edges
    .map(edge => {
      const dst = graph.getNode(edge.dst)
      if (dst === undefined || edge.attrs?.range === undefined) return undefined
      // ADR-0014 §4.F4 — workspace edges: yarn-classic lacks `workspace:`
      // protocol; drop the specifier and emit `resolvedVersion` (or fire
      // RECIPE_WORKSPACE_UNRESOLVED when no resolved version is available).
      // Empty pending specifier carried nothing to drop — gate via
      // shouldEmitWorkspaceResolved per ADR-0014 §5:412.
      if (isWorkspaceEdge(edge)) {
        const ws = workspaceRangeOfEdge(edge, dst)
        if (ws !== undefined) {
          const resolved = stringifyForVersionOnly(ws)
          if (resolved === undefined) {
            emitWorkspaceUnresolved(
              { src: edge.src, dst: edge.dst, kind: edge.kind },
              emitDiagnostic,
            )
            return undefined
          }
          if (shouldEmitWorkspaceResolved(ws)) {
            emitWorkspaceResolved(
              { src: edge.src, dst: edge.dst, kind: edge.kind },
              ws.specifier,
              resolved,
              emitDiagnostic,
            )
          }
          return [dst.name, resolved] as [string, string]
        }
      }
      // An npm-aliased edge emits under the alias name, writing its dependency line
      // name with the verbatim `npm:<target>@<range>` locator as the value
      // (`<alias> "npm:<target>@<range>"`), so reparse re-binds it via the
      // alias-keyed `specIndex` descriptor. A canonical edge uses the target's
      // own name.
      return [edge.attrs.alias ?? dst.name, edge.attrs.range] as [string, string]
    })
    .filter((entry): entry is [string, string] => entry !== undefined)
    .sort((a, b) => cmpUtf16(a[0], b[0]))
}

// F8 — the verbatim absent-target dep refs captured for `nodeId` in the given
// block, or `[]` when none (no sidecar / no refs for that node+block). The
// `block` filter routes a `dep`-kind drop to the `dependencies:` emit and an
// `optional`-kind drop to `optionalDependencies:`.
function unresolvedDepRefsOfNode(
  sidecar: YarnClassicSidecar | undefined,
  nodeId: string,
  block: 'dependencies' | 'optionalDependencies',
): UnresolvedDepRef[] {
  const refs = sidecar?.unresolvedDeps?.get(nodeId)
  return refs === undefined ? [] : refs.filter(ref => ref.block === block)
}

// F8 — merge the verbatim absent-target refs into a live `[name, range]` block,
// then re-sort by name to match yarn's alphabetical block order. A LIVE edge of
// the same dep-name always wins (the ref is skipped) so a re-emitted edge is
// never clobbered by a stale verbatim line. Mirrors the yarn-berry
// `withUnresolvedDepRefs` absent-target merge-then-sort.
function withUnresolvedDepRefs(
  liveBlock: Array<[string, string]>,
  refs: readonly UnresolvedDepRef[],
): Array<[string, string]> {
  if (refs.length === 0) return liveBlock
  const byName = new Map<string, string>(liveBlock)
  for (const ref of refs) {
    if (!byName.has(ref.name)) byName.set(ref.name, ref.range)
  }
  return Array.from(byName.entries()).sort((a, b) => cmpUtf16(a[0], b[0]))
}

function reportDroppedPeerEdges(
  graph: Graph,
  srcId: string,
  warned: Set<string>,
  emitDiagnostic: (diagnostic: Diagnostic) => void,
): void {
  for (const edge of graph.out(srcId, 'peer')) {
    const key = `${edge.src}\u0000${edge.dst}\u0000${edge.attrs?.range ?? ''}`
    if (warned.has(key)) continue
    warned.add(key)

    const dst = graph.getNode(edge.dst)
    emitDiagnostic({
      code: 'YARN_CLASSIC_PEER_DROPPED',
      severity: 'warning',
      subject: srcId,
      message: dst === undefined || edge.attrs?.range === undefined
        ? `peer edge ${edge.src} -> ${edge.dst} is unsupported in yarn-classic; dropping on emit`
        : `peer edge ${edge.src} -> ${dst.name}@${edge.attrs.range} is unsupported in yarn-classic; dropping on emit`,
    })
  }
}

function reportPeerContextFlatten(
  node: Node,
  warned: Set<string>,
  emitDiagnostic: (diagnostic: Diagnostic) => void,
): void {
  if (node.peerContext.length === 0 || warned.has(node.id)) return
  warned.add(node.id)
  emitDiagnostic({
    code: 'YARN_CLASSIC_PEER_VIRT_FLATTENED',
    severity: 'warning',
    subject: node.id,
    message: `peerContext ${JSON.stringify(node.peerContext)} is unsupported in yarn-classic; flattening on emit`,
  })
}

function reportPatchDrop(
  node: Node,
  warned: Set<string>,
  emitDiagnostic: (diagnostic: Diagnostic) => void,
): void {
  if (node.patch === undefined || warned.has(node.id)) return
  warned.add(node.id)
  patchEmitDropped(
    node.id,
    'patch',
    `yarn-classic has no patch: protocol; ${JSON.stringify(node.patch)} dropped`,
    emitDiagnostic,
  )
}

