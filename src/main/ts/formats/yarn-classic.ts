export function parseEntries(input: string): YarnClassicEntry[] {
  const entries: YarnClassicEntry[] = []
  const lines = input.split('\n')
  let current: YarnClassicEntry | undefined
  let block: 'dependencies' | 'optionalDependencies' | undefined

  // Start at 0 and let the loop's own `#`-comment and blank-line skipping
  // consume the header block. Old yarn (e.g. lodash's lock) omits the blank
  // line(s) between `# yarn lockfile v1` and the first entry, so a fixed
  // header-length offset would mis-align and orphan the first entry's body.
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i] ?? ''
    if (line.length === 0) {
      block = undefined
      continue
    }
    if (line.startsWith('#')) continue

    if (!line.startsWith(' ')) {
      current = parseEntryHeaderLine(line)
      entries.push(current)
      block = undefined
      continue
    }

    if (current === undefined) {
      throw parseFailed(`body line without entry header (${JSON.stringify(line)})`)
    }

    if (line.startsWith('    ')) {
      captureDependencyLine(current, block, line)
      continue
    }

    if (!line.startsWith('  ')) {
      throw parseFailed(`unexpected indent (${JSON.stringify(line)})`)
    }

    if (line === '  dependencies:') {
      block = 'dependencies'
      continue
    }
    if (line === '  optionalDependencies:') {
      block = 'optionalDependencies'
      continue
    }

    block = undefined
    // A QUOTED field-key (`  "version" "1.0.0"`, `  "resolved" "…"`) is not a
    // shape yarn 1 emits — its lockfile writer quotes string VALUES but never
    // field KEYS (`version` / `resolved` / `integrity` begin with a letter and
    // are emitted bare). A quoted key at field position is the signature of a
    // yarn-error.log "Lockfile:" dump (yarn's reporter re-indents the lockfile
    // under that heading) or an otherwise mangled, non-lockfile input. Reject
    // cleanly with a diagnostic that names the likely source, rather than
    // silently capturing it as an unknown-field extra and later throwing a
    // misleading "missing version"; a malformed entry must not become a clean node, so do
    // NOT bend the parser to accept non-lockfiles).
    if (line.startsWith('  "')) {
      throw parseFailed(
        `quoted field-key ${JSON.stringify(line.trimStart())} is not valid yarn-classic `
          + `(yarn 1 never quotes entry field names); input looks like a yarn-error.log dump `
          + `or a mangled lockfile, not a yarn.lock`,
      )
    }
    if (line.startsWith('  version ')) {
      captureEntryFieldLine(current, 'version', line)
      continue
    }
    if (line.startsWith('  resolved ')) {
      captureEntryFieldLine(current, 'resolved', line)
      continue
    }
    if (line.startsWith('  integrity ')) {
      captureEntryFieldLine(current, 'integrity', line)
      continue
    }

    captureEntryFieldLine(current, 'extra', line)
    continue
  }

  return entries
}

export function parseDependencyLine(line: string): [string, string] {
  const raw = line.slice(4)
  if (!DEP_PAIR_RE.test(raw)) {
    throw parseFailed(`malformed dependency line (${JSON.stringify(line)})`)
  }

  const splitAt = splitDependencyPair(raw)
  const nameToken = raw.slice(0, splitAt)
  const rangeToken = raw.slice(splitAt + 1)
  return [
    nameToken.startsWith('"') ? parseQuotedToken(nameToken) : nameToken,
    rangeToken.startsWith('"') ? parseQuotedToken(rangeToken) : rangeToken,
  ]
}

export function parseSpec(spec: string): { name: string; spec: string } {
  const idx = spec.indexOf('@', spec.startsWith('@') ? 1 : 0)
  // A bare package-name entry key (`foo:` with no `@<range>`) is NOT a shape
  // yarn 1 emits — its lockfile writer always keys entries by `<name>@<range>`
  // descriptors; malformed descriptors are rejected. Use a clear,
  // typed diagnostic that names the missing `@<range>`, rather than the generic
  // "bad entry-spec" message, so the failure is actionable.
  if (idx < 0) {
    throw parseFailed(
      `entry key ${JSON.stringify(spec)} is a bare package name with no '@<range>' descriptor; `
        + `yarn-classic keys must be '<name>@<range>'`,
    )
  }
  if (idx === 0 || idx === spec.length - 1) {
    throw parseFailed(`bad entry-spec ${JSON.stringify(spec)}`)
  }
  return {
    name: spec.slice(0, idx),
    spec: spec.slice(idx + 1),
  }
}

// Entry-key package identity carried by one descriptor.
//
// yarn 1 encodes an npm-alias as `"<alias>@npm:<target>@<range>"` (the dep is
// declared under `<alias>` but resolves to the registry package `<target>`).
// The RESOLVED package — the node identity — is `<target>`, NOT `<alias>`;
// `<alias>` lives only as the consumer-facing descriptor name. Keying the node
// off `<alias>` (as the bare `parseSpec().name` does) splits the npm-aliased
// entry into a phantom `<alias>@<version>` node DISTINCT from the real
// `<target>@<version>`, duplicating it. So:
//   - `resolvedName` is the node-identity name — `<target>` for an `npm:` alias,
//     else the descriptor's own name;
//   - `aliasName` is the consumer-facing alias (`<alias>`), or `undefined` for a
//     canonical (non-aliased) descriptor;
//   - `descriptorKey` is the verbatim `<part.name>@<part.spec>` join — the exact
//     entry-key spec, indexed into `specIndex` so a consumer that declares the
//     alias range resolves to the same node, and preserved on the entry key.
//
// Only the `npm:` protocol aliases an identity. A bare/`file:`/`link:`/`git…`
// spec keeps its own name. The alias TARGET may itself be scoped
// (`npm:@scope/pkg@^1`).
export function specIdentity(part: { name: string; spec: string }): {
  resolvedName: string
  aliasName?: string
  descriptorKey: string
} {
  const descriptorKey = `${part.name}@${part.spec}`
  if (part.spec.startsWith('npm:')) {
    const target = part.spec.slice('npm:'.length)
    // An alias REQUIRES the `npm:` body to be a full `<target-name>@<range>`
    // locator — a name AND an embedded `@<range>` separator. The target name may
    // be scoped (`npm:@scope/pkg@^1`).
    //
    // CRUCIAL boundary vs the yarn-BERRY default-protocol form `npm:<range>`
    // (`undici-types@npm:~5.26.4`): there the body is a BARE range with NO
    // embedded `@`, so it is NOT an alias — the resolved name is the descriptor's
    // own name. Treating `~5.26.4` as a "target package name" would mint a
    // phantom `~5.26.4@<version>` node. Therefore no embedded `@` means a
    // default-protocol range, never
    // an alias.
    const at = target.indexOf('@', target.startsWith('@') ? 1 : 0)
    const targetName = at > 0 ? target.slice(0, at) : undefined
    if (targetName !== undefined && targetName.length > 0 && targetName !== part.name) {
      return { resolvedName: targetName, aliasName: part.name, descriptorKey }
    }
    // A self-referential `npm:` alias to the SAME name (`foo@npm:foo@^1`), or a
    // bare default-protocol range (`foo@npm:^1`), is not a rename — no alias edge
    // attr, identity unchanged.
    return { resolvedName: part.name, descriptorKey }
  }
  return { resolvedName: part.name, descriptorKey }
}

// A classic descriptor range is a registry range (bare semver or `npm:`-aliased)
// — the only class Rung-3 semver + the resolutions-pin INFO hint apply to. A
// `file:`/`link:`/`git…`/URL range carries an explicit non-`npm:` protocol.
export function isClassicRegistryRange(range: string): boolean {
  if (range.startsWith('npm:')) return true
  const colonIdx = range.indexOf(':')
  if (colonIdx <= 0) return true // bare semver / tag
  return !/^[a-z][a-z0-9+.-]*$/i.test(range.slice(0, colonIdx))
}

/** Whether an entry-key descriptor `<name-or-alias>@<range>` still resolves to `version` —
 *  the range is the tail after the LAST `@` (scoped names / npm-aliases keep the range
 *  after their final `@`). A non-semver range (unparseable) does not carry. */
export function descriptorSatisfies(descriptor: string, version: string): boolean {
  const at = descriptor.lastIndexOf('@')
  if (at <= 0) return false
  try { return semver.satisfies(version, descriptor.slice(at + 1)) } catch { return false }
}

// `<file|link|portal>:<path>` → { protocol, path }. Returns undefined for
// non-local shapes (URLs). `path` keeps the body verbatim (leading `./` / `../`
// preserved) so the directory canonical mirrors the on-disk specifier.
export function parseLocalSpec(
  resolved: string,
): { protocol: 'file' | 'link' | 'portal'; path: string } | undefined {
  if (!isYarnClassicLocalSpec(resolved)) return undefined
  const colon = resolved.indexOf(':')
  return {
    protocol: resolved.slice(0, colon) as 'file' | 'link' | 'portal',
    path: resolved.slice(colon + 1),
  }
}

// Canonical resolution for a yarn-classic `resolved` value. Local specifiers
// (`file:` / `link:` / `portal:`) canonicalize to a `directory` resolution
// adapter-side: the shared recipe deliberately routes bare `link:` to `unknown`
// (recipe/resolution.ts) because in pnpm `link:` is a WORKSPACE shape — a
// different meaning — so yarn-classic must NOT lean on the recipe for `link:` /
// `portal:` or it would raise a spurious RECIPE_RESOLUTION_UNKNOWN. `file:`
// already maps to `directory` in the recipe, but is handled here too for a
// single code path. Everything else delegates to the recipe (git / tarball /
// codeload / …).
export function canonicalResolutionOfResolved(
  resolved: string,
): import('../recipe/resolution.ts').ResolutionCanonical {
  const local = parseLocalSpec(resolved)
  if (local !== undefined) {
    return { type: 'directory', path: local.path }
  }
  return parseResolutionRecipe(resolved, { sourceKind: 'yarn-classic-resolved' })
}

export function parseResolution(input: string): string {
  // git deps are first-class in yarn-classic; a single git entry must not abort
  // the parse. Relative `file:` / `link:` / `portal:` local specifiers are
  // genuine opaque references. The opaque string is stored VERBATIM on
  // Node.resolution and re-emitted byte-for-byte by `formatResolution`; the
  // canonical resolution is derived separately via `canonicalResolutionOfResolved`.
  if (isYarnClassicResolvableUrl(input) || isYarnClassicLocalSpec(input)) {
    return input
  }
  throw parseFailed(`unsupported resolved URL ${JSON.stringify(input)}`)
}

function createClassicParseContext(options: YarnClassicParseOptions): ClassicParseContext {
  return {
    builder: newBuilder(),
    diagnostics: [],
    specIndex: new Map(),
    sidecar: new Map(),
    entryExtras: new Map(),
    // F8 — Rung-4-dropped refs are re-emitted from the matching inner block.
    unresolvedDeps: new Map(),
    unknownFields: new Set(),
    entryNodes: [],
    seenEntries: new Map(),
    // Source-gated max-satisfying candidates by package name (ADR-0025).
    semverCandidatesByName: new Map(),
    options,
  }
}

function addClassicEntryNodes(context: ClassicParseContext, entries: readonly YarnClassicEntry[]): void {
  for (const entry of entries) addClassicEntryNodeGroups(context, entry)
}

function addClassicEntryNodeGroups(context: ClassicParseContext, entry: YarnClassicEntry): void {
  const specs = splitEntryKey(entry.key)
  if (specs.length === 0) throw parseFailed(`entry ${JSON.stringify(entry.key)} has empty spec list`)
  const version = entry.version
  if (version === undefined) throw parseFailed(`entry ${JSON.stringify(entry.key)} missing version`)
  for (const [name, descriptors] of classicSpecsByResolvedName(specs)) {
    addClassicResolvedEntry(context, entry, name, version, descriptors)
  }
}

// Alias and plain descriptors share one resolved-package identity while retaining
// every verbatim entry-key descriptor.
function classicSpecsByResolvedName(specs: readonly string[]): Map<string, string[]> {
  const byName = new Map<string, string[]>()
  for (const spec of specs) {
    const identity = specIdentity(parseSpec(spec))
    const descriptors = byName.get(identity.resolvedName)
    if (descriptors === undefined) byName.set(identity.resolvedName, [identity.descriptorKey])
    else descriptors.push(identity.descriptorKey)
  }
  return byName
}

function addClassicResolvedEntry(
  context: ClassicParseContext,
  entry: YarnClassicEntry,
  name: string,
  version: string,
  rawDescriptors: readonly string[],
): void {
  const descriptors = Array.from(new Set(rawDescriptors)).sort(cmpUtf16)
  const canonicalResolution = entry.resolved === undefined
    ? undefined
    : canonicalResolutionOfResolved(entry.resolved)
  const effectiveSource = canonicalResolution === undefined
    ? undefined
    : sourceDiscriminatorOf(canonicalResolution)
  const id = serializeNodeId(name, version, [], undefined, effectiveSource)
  if (mergeClassicDuplicateEntry(context, entry, id, descriptors)) return

  context.seenEntries.set(id, { resolved: entry.resolved, integrity: entry.integrity })
  const node: Node = { id, name, version, peerContext: [] }
  if (effectiveSource !== undefined) node.source = effectiveSource
  context.builder.addNode(node)

  if (canonicalResolution?.type === 'unknown') {
    context.diagnostics.push(unknownResolutionDiagnostic(id, entry.resolved!))
  }
  registerClassicSemverCandidate(context, node, canonicalResolution?.type ?? 'absent')
  recordClassicTarball(context, entry, node, canonicalResolution)
  recordClassicEntrySidecars(context, entry, node, descriptors)
}

function mergeClassicDuplicateEntry(
  context: ClassicParseContext,
  entry: YarnClassicEntry,
  id: string,
  descriptors: readonly string[],
): boolean {
  const prior = context.seenEntries.get(id)
  if (prior === undefined) return false
  if (prior.resolved !== entry.resolved || prior.integrity !== entry.integrity) {
    throw new LockfileError({
      code: 'IRREDUCIBLE_LOSS',
      message: `two entries collapse onto NodeId ${id} with differing resolution`,
    })
  }
  const merged = context.sidecar.get(id) ?? []
  for (const spec of descriptors) {
    if (!merged.includes(spec)) merged.push(spec)
    context.specIndex.set(spec, id)
  }
  context.sidecar.set(id, merged)
  return true
}

function registerClassicSemverCandidate(
  context: ClassicParseContext,
  node: Node,
  sourceType: SemverCandidate['sourceType'],
): void {
  const candidate: SemverCandidate = { id: node.id, version: node.version, sourceType }
  const candidates = context.semverCandidatesByName.get(node.name)
  if (candidates === undefined) context.semverCandidatesByName.set(node.name, [candidate])
  else candidates.push(candidate)
}

function recordClassicTarball(
  context: ClassicParseContext,
  entry: YarnClassicEntry,
  node: Node,
  canonicalResolution: ReturnType<typeof canonicalResolutionOfResolved> | undefined,
): void {
  const nativeResolution = entry.resolved === undefined ? undefined : parseResolution(entry.resolved)
  if (entry.integrity === undefined) {
    if (canonicalResolution !== undefined) {
      context.builder.setTarball(
        { name: node.name, version: node.version, source: node.source },
        { resolution: canonicalResolution, nativeResolution },
      )
    }
    return
  }
  const integrity = parseSri(entry.integrity, 'sri')
  if (isEmptyIntegrity(integrity)) {
    context.diagnostics.push(invalidIntegrityDiagnostic('YARN_CLASSIC', node.id, entry.integrity))
    return
  }
  const payload: { integrity: Integrity; resolution?: typeof canonicalResolution; nativeResolution?: string } = { integrity }
  if (canonicalResolution !== undefined) payload.resolution = canonicalResolution
  if (nativeResolution !== undefined) payload.nativeResolution = nativeResolution
  context.builder.setTarball({ name: node.name, version: node.version, source: node.source }, payload)
}

function recordClassicEntrySidecars(
  context: ClassicParseContext,
  entry: YarnClassicEntry,
  node: Node,
  descriptors: readonly string[],
): void {
  context.sidecar.set(node.id, descriptors.slice())
  if (entry.extras !== undefined && entry.extras.length > 0) {
    context.entryExtras.set(node.id, entry.extras)
    for (const raw of entry.extras) context.unknownFields.add(raw.split(' ', 1)[0] ?? raw)
  }
  context.entryNodes.push({ node, entry })
  for (const spec of descriptors) context.specIndex.set(spec, node.id)
}

function reportClassicUnknownFields(context: ClassicParseContext): void {
  if (context.unknownFields.size === 0) return
  context.diagnostics.push({
    code: 'YARN_CLASSIC_UNKNOWN_FIELD',
    severity: 'info',
    subject: 'yarn-classic',
    message: `preserved unmodelled yarn-classic entry field(s) verbatim for round-trip: ${Array.from(context.unknownFields).sort(cmpUtf16).join(', ')}`,
  })
}

function addClassicEntryEdges(context: ClassicParseContext): void {
  const ladder: ClassicEdgeLadderContext = {
    candidatesByName: context.semverCandidatesByName,
    overrides: context.options.overrides ?? [],
    manifestsProvided: context.options.overrides !== undefined,
  }
  for (const { node, entry } of context.entryNodes) {
    addEdgesFromMap(context.builder, context.diagnostics, node.id, entry.dependencies, 'dep', context.specIndex, ladder, context.unresolvedDeps)
    addEdgesFromMap(context.builder, context.diagnostics, node.id, entry.optionalDependencies, 'optional', context.specIndex, ladder, context.unresolvedDeps)
  }
}

function sealClassicParse(context: ClassicParseContext): Graph {
  for (const diagnostic of context.diagnostics) context.builder.diagnostic(diagnostic)
  try {
    const graph = context.builder.seal()
    const parsedSidecar = buildSidecar(context.sidecar, context.entryExtras, context.unresolvedDeps)
    rememberSidecar(graph, parsedSidecar)
    return isEmptySidecar(parsedSidecar) ? graph : withSidecarPropagation(graph, parsedSidecar)
  } catch (error) {
    if (error instanceof GraphError) {
      throw new LockfileError({ code: 'PARSE_FAILED', message: `seal failed: ${error.message}` })
    }
    throw error
  }
}

// Identity-preserving NodeId re-key through an old→new id map (built from a
// mutate's ChangeRecords for IDENTITY-PRESERVING renames only), then membership-
// prune to the surviving node set. Mirrors berry's `remapSidecar`: an oldId in
// `nextNodes` is rewritten to the new node's id; an oldId absent from `nextNodes`
// keeps its own id (and is dropped when that id is gone — the version-bump reset).
function remapSidecar(
  sidecar: YarnClassicSidecar,
  nextNodes: Map<string, Node>,
  bumpedNodes: Map<string, Node>,
  graph: Graph,
): YarnClassicSidecar {
  const remap = <T>(m: Map<string, T[]>, clone: (v: T) => T): Map<string, T[]> => {
    const next = new Map<string, T[]>()
    for (const [oldId, vals] of m) {
      const nextId = nextNodes.get(oldId)?.id ?? oldId
      if (graph.getNode(nextId) !== undefined) next.set(nextId, vals.map(clone))
    }
    return next
  }
  const identityStr = (s: string): string => s
  const entrySpecs = remap(sidecar.entrySpecs, identityStr)
  // Carry entry-key DESCRIPTORS across a same-name version BUMP when the range still
  // SATISFIES the new version. A caret/tilde range applies to a RANGE of versions
  // (`lodash@^4.17.0` survives 4.17.11→4.18.0), so dropping it ORPHANS the consumer's
  // declared range → the key resets to `name@version` and yarn's `--frozen-lockfile`
  // re-resolves. An exact pin (`lodash@4.17.11`) does NOT satisfy 4.18.0
  // and correctly does not carry (the manifest pin changed too). Only the entry-KEY set
  // carries; other sidecar fields (extras / unresolvedDeps) stay version-scoped and reset.
  for (const [oldId, newNode] of bumpedNodes) {
    if (graph.getNode(newNode.id) === undefined) continue
    const carried = (sidecar.entrySpecs.get(oldId) ?? []).filter(d => descriptorSatisfies(d, newNode.version))
    if (carried.length === 0) continue
    const merged = new Set(entrySpecs.get(newNode.id) ?? [])
    for (const d of carried) merged.add(d)
    entrySpecs.set(newNode.id, [...merged])
  }
  const entryExtras = sidecar.entryExtras !== undefined ? remap(sidecar.entryExtras, identityStr) : undefined
  const unresolvedDeps = sidecar.unresolvedDeps !== undefined
    ? remap(sidecar.unresolvedDeps, (r: UnresolvedDepRef) => ({ ...r }))
    : undefined
  return buildSidecar(entrySpecs, entryExtras, unresolvedDeps)
}

/**
 * Wraps a Graph so that every `.mutate()` call re-attaches the yarn-classic
 * key-descriptor sidecar (the verbatim parse-time entry-key set + unknown-field
 * extras, keyed by NodeId) to the brand-new Graph instance `mutate()` returns —
 * id-REMAPPED for identity-preserving renames then membership-pruned to that
 * graph's surviving node set via `remapSidecar`.
 *
 * This mirrors the yarn-BERRY `withSidecarPropagation` wrapper in
 * `_yarn-berry-core.ts` (which propagates peerDependencies / conditions /
 * metadata the same way). Without it, the sidecar is a per-graph WeakMap and a
 * bare `graph.mutate()` (called by EVERY modify primitive —
 * removeDependency/replaceVersion/addDependency — and by `optimize`/`enrich`)
 * builds a fresh Graph with no sidecar entry, so `entrySpecsOfNode` loses the
 * verbatim entry-key descriptors and the verbatim orphan descriptor ranges drop again on
 * the audit-fix path (the react@557e28f data loss returns). With the wrapper, a
 * surviving (non-renamed) node keeps its full on-disk descriptor set across
 * parse → modify → stringify.
 *
 * Identity-preserving NodeId re-key: a rename (a peerContext shift that re-keys the
 * `(...)` suffix but keeps the `name@version` base) CARRIES its key descriptors
 * onto the new id: the old→new pair is read off the mutate's ChangeRecords and
 * threaded through `remapSidecar`, so the renamed node keeps its verbatim
 * entry-key set across parse → modify → stringify (this is the gap that dropped
 * sidecar fidelity for renamed nodes on the audit-fix path).
 *
 * A node that is itself version-BUMPED by the modify (e.g. `replaceVersion` of
 * the very node whose key held orphans → a NEW name@version NodeId) legitimately
 * RESETS its key: the base key differs, so the old→new guard below leaves it
 * UNMAPPED, its old NodeId is absent from the next graph, and `remapSidecar`'s
 * membership check drops that entry — the new node reconstructs its key from live
 * edges (+ any fresh sidecar). The old version's orphan ranges do not apply to
 * the new version, so they correctly do NOT carry across the bump.
 */
function withSidecarPropagation(graph: Graph, sidecar: YarnClassicSidecar): Graph {
  // Thin delegation proxy — only `mutate` is overridden; everything else
  // forwards to the underlying graph so the object is still a true Graph.
  const proxy: Graph = {
    getNode:     (...args) => graph.getNode(...args),
    nodes:       ()        => graph.nodes(),
    byName:      (...args) => graph.byName(...args),
    roots:       ()        => graph.roots(),
    out:         (...args) => graph.out(...args),
    in:          (...args) => graph.in(...args),
    walk:        (...args) => graph.walk(...args),
    topoSort:    ()        => graph.topoSort(),
    subgraph:    (...args) => graph.subgraph(...args),
    diff:        (...args) => graph.diff(...args),
    tarball:     (...args) => graph.tarball(...args),
    tarballOf:   (...args) => graph.tarballOf(...args),
    tarballs:    ()        => graph.tarballs(),
    diagnostics: ()        => graph.diagnostics(),
    layoutHints: ()        => graph.layoutHints(),
    mutate(transaction: (m: Mutator) => void): MutateResult {
      const result = graph.mutate(transaction)
      // Empty-sidecar fast-path (mirrors berry's `withSidecarPropagation`): with
      // nothing to carry there is no point remapping or re-wrapping — return the
      // bare mutate result and skip the proxy allocation entirely.
      if (isEmptySidecar(sidecar)) return result
      // Identity-preserving NodeId re-key from the applied ChangeRecords for
      // IDENTITY-PRESERVING renames only (`name@version[+…]` base unchanged — a
      // peerContext shift). A genuine version/identity change is left UNMAPPED so
      // its entry membership-prunes (the old version's descriptors do not apply to
      // the new node — it resets to a key reconstructed from live edges). Surviving
      // (non-renamed) NodeIds map to themselves and keep their verbatim descriptors.
      const nextNodes  = new Map<string, Node>()   // identity-preserving → carry verbatim
      const bumpedNodes = new Map<string, Node>()   // same-name version bump → carry in-range descriptors
      for (const rec of result.applied) {
        if (rec.kind !== 'node-replaced' && rec.kind !== 'peer-context-replaced') continue
        const oldId = rec.oldSubject
        if (oldId === undefined) continue // id unchanged — no remap needed
        const newNode = result.graph.getNode(rec.subject)
        if (newNode === undefined) continue
        if (stripPeerContextFromNodeId(oldId) === stripPeerContextFromNodeId(rec.subject)) {
          nextNodes.set(oldId, newNode)              // peerContext shift — carry the full descriptor set
        } else if (nameOf(oldId) === newNode.name) {
          bumpedNodes.set(oldId, newNode)            // version BUMP of the same package — carry ranges that still satisfy
        }
        // else: a genuine name/identity change — the old descriptors do not apply; drop.
      }
      const nextSidecar = remapSidecar(sidecar, nextNodes, bumpedNodes, result.graph)
      rememberSidecar(result.graph, nextSidecar)
      // Re-wrap the new graph so a SUBSEQUENT mutate propagates the sidecar too
      // (the modify primitives chain several mutate calls).
      return { ...result, graph: withSidecarPropagation(result.graph, nextSidecar) }
    },
  }
  // Register the sidecar on the proxy instance too so stringify works when
  // called with the proxy directly (not just with the underlying graph).
  rememberSidecar(proxy, sidecar)
  return proxy
}

// === Parse helpers ==========================================================

function parseEntryHeaderLine(line: string): YarnClassicEntry {
  if (!line.endsWith(':')) {
    throw parseFailed(`top-level entry line must end with ':' (${JSON.stringify(line)})`)
  }
  return {
    key: parseEntryKeyToken(line.slice(0, -1)),
    dependencies: new Map(),
    optionalDependencies: new Map(),
  }
}

function captureDependencyLine(
  entry: YarnClassicEntry,
  block: 'dependencies' | 'optionalDependencies' | undefined,
  line: string,
): void {
  if (block === undefined) {
    throw parseFailed(`dependency line outside dependency block (${JSON.stringify(line)})`)
  }
  const [name, range] = parseDependencyLine(line)
  entry[block].set(name, range)
}

function captureEntryFieldLine(
  entry: YarnClassicEntry,
  field: 'version' | 'resolved' | 'integrity' | 'extra',
  line: string,
): void {
  if (field === 'version') {
    entry.version = parseQuotedToken(line.slice('  version '.length))
    return
  }
  if (field === 'resolved') {
    entry.resolved = parseQuotedToken(line.slice('  resolved '.length))
    return
  }
  if (field === 'integrity') {
    // A single integrity hash stays bare
    // (`integrity sha512-…`) but QUOTED when it is a space-separated
    // multi-hash SRI (`integrity "sha1-… sha512-…"`) — yarn 1 quotes any
    // value containing a space. A quoted value must be un-quoted here, else
    // the surrounding `"` glue onto the first/last members and `parseSri`
    // skips every hash (silent integrity loss — the sha1 AND the sha512 both
    // vanish). Un-quote into the bare SRI string so the shared multi-hash
    // parser preserves every algorithm (spec/formats/_common.md §3.1).
    const value = line.slice('  integrity '.length)
    entry.integrity = value.startsWith('"') ? parseQuotedToken(value) : value
    return
  }

  // Unknown scalar entry field — e.g. yarn-1's legacy `uid ""` on link:/file:
  // entries. A universal converter must not reject a valid lockfile over an
  // unmodelled field: capture the line VERBATIM (sans the 2-space indent) for
  // faithful round-trip. The parse surfaces one info diagnostic listing the
  // unmodelled field names.
  entry.extras ??= []
  entry.extras.push(line.slice(2))
}

function ensureClassicHeader(input: string): void {
  if (check(input)) return
  if (BERRY_HEADER_RE.test(input)) {
    throw new LockfileError({
      code: 'FORMAT_MISMATCH',
      message: 'expected yarn-classic lockfile header, got yarn-berry metadata header',
    })
  }
  const lines = input.split('\n')
  if (lines[0] !== HEADER_LINE_1 || lines[1] !== HEADER_LINE_2) {
    throw new LockfileError({
      code: 'FORMAT_MISMATCH',
      message: 'expected yarn-classic lockfile v1 header',
    })
  }
  throw new LockfileError({
    code: 'PARSE_FAILED',
    message: 'yarn-classic header is malformed',
  })
}

function splitDependencyPair(raw: string): number {
  let escaped = false
  let quoted = false
  for (let i = 0; i < raw.length; i++) {
    const char = raw[i]
    if (escaped) {
      escaped = false
      continue
    }
    if (char === '\\') {
      escaped = true
      continue
    }
    if (char === '"') {
      quoted = !quoted
      continue
    }
    if (char === ' ' && !quoted) {
      return i
    }
  }
  throw parseFailed(`malformed dependency pair (${JSON.stringify(raw)})`)
}

// === Edge resolution ========================================================

function resolveClassicEdgeTarget(
  diagnostics: Diagnostic[],
  srcId: string,
  name: string,
  range: string,
  specIndex: ReadonlyMap<string, string>,
  ladder: ClassicEdgeLadderContext,
): ClassicEdgeResolution {
  // Rung 0 — exact specIndex match (UNCHANGED, O(1)).
  let dstId = specIndex.get(`${name}@${range}`)
  // Rung 2 — OVERRIDE-MAP forced link. Authoritative pin (`resolutions`) beats
  // semver inference; handles a NON-satisfying pin. The `to` target is fed back
  // through Rung 0 (`specIndex.get`); a target classic can't key (a `patch:`
  // descriptor) falls through to Rung 3 on the ORIGINAL range — never throws.
  if (dstId === undefined && ladder.overrides.length > 0) {
    const to = overrideTargetFor(name, range, [nameOf(srcId)], ladder.overrides)
    if (to !== undefined) {
      const viaOverride = specIndex.get(`${name}@${to}`)
      if (viaOverride !== undefined) dstId = viaOverride
    }
  }
  if (dstId !== undefined) return { status: 'bound', dstId }
  return resolveClassicSemverTarget(diagnostics, srcId, name, range, ladder)
}

function resolveClassicSemverTarget(
  diagnostics: Diagnostic[],
  srcId: string,
  name: string,
  range: string,
  ladder: ClassicEdgeLadderContext,
): ClassicEdgeResolution {
  // Rung 3 — SOURCE-GATED max-satisfying semver (tarball candidates only).
  const semverResult = semverResolve(range, ladder.candidatesByName.get(name) ?? [])
  if (semverResult.kind === 'bound') return { status: 'bound', dstId: semverResult.id }
  if (semverResult.kind === 'ambiguous') {
    diagnostics.push(ambiguousResolutionDiagnostic(
      'YARN_CLASSIC', srcId, name, range, semverResult.candidateIds,
    ))
    return { status: 'ambiguous' }
  }
  return resolveClassicDistTagTarget(diagnostics, srcId, name, range, ladder)
}

function resolveClassicDistTagTarget(
  diagnostics: Diagnostic[],
  srcId: string,
  name: string,
  range: string,
  ladder: ClassicEdgeLadderContext,
): ClassicEdgeResolution {
  // Rung 3.5 — dist-tag binding. A registry range that is a published
  // dist-TAG (`latest` / `next`), not a semver range, binds the UNIQUE registry
  // sibling. A multi-version tag diagnoses and drops; channel pointers are
  // never guessed.
  const result = distTagResolve(range, ladder.candidatesByName.get(name) ?? [])
  if (result.kind === 'bound') return { status: 'bound', dstId: result.id }
  if (result.kind !== 'ambiguous') return { status: 'missing' }
  diagnostics.push(ambiguousResolutionDiagnostic('YARN_CLASSIC', srcId, name, range, result.candidateIds))
  if (!ladder.manifestsProvided) {
    diagnostics.push(resolutionPinUnresolvedDiagnostic('YARN_CLASSIC', srcId, name, range))
  }
  return { status: 'ambiguous' }
}

function preserveClassicMissingDependency(
  diagnostics: Diagnostic[],
  srcId: string,
  name: string,
  range: string,
  kind: 'dep' | 'optional',
  ladder: ClassicEdgeLadderContext,
  unresolvedDeps: Map<string, UnresolvedDepRef[]> | undefined,
): void {
  // Rung 4 — drop. Add the resolutions-pin INFO hint when a registry range
  // missed everything and no manifests were supplied.
  diagnostics.push({
    code: 'YARN_CLASSIC_MISSING_ENTRY',
    severity: 'warning',
    subject: srcId,
    message: `missing lockfile entry for ${name}@${range}`,
  })
  if (!ladder.manifestsProvided
    && isClassicRegistryRange(range)
    && (ladder.candidatesByName.get(name)?.length ?? 0) > 0) {
    diagnostics.push(resolutionPinUnresolvedDiagnostic('YARN_CLASSIC', srcId, name, range))
  }
  if (unresolvedDeps === undefined) return
  const refs = unresolvedDeps.get(srcId) ?? []
  refs.push({
    block: kind === 'optional' ? 'optionalDependencies' : 'dependencies',
    name,
    range,
  })
  unresolvedDeps.set(srcId, refs)
}

function classicResolvedEdgeAttrs(
  srcId: string,
  dstId: string,
  name: string,
  range: string,
  ladder: ClassicEdgeLadderContext,
): EdgeAttrs {
  const dstName = nameOf(dstId)
  const attrs: EdgeAttrs = { range }
  if (name !== dstName) attrs.alias = name
  const overrideRange = overrideTargetFor(name, range, [nameOf(srcId)], ladder.overrides)
  if (overrideRange !== undefined) attrs.overrideRange = overrideRange
  return attrs
}

function addEdgesFromMap(
  builder: ReturnType<typeof newBuilder>,
  diagnostics: Diagnostic[],
  srcId: string,
  deps: Map<string, string>,
  kind: 'dep' | 'optional',
  specIndex: Map<string, string>,
  ladder: ClassicEdgeLadderContext,
  // F8 — collector for Rung-4-dropped refs whose target is ABSENT from the lock.
  // Appended to (keyed by srcId) so emit re-emits them verbatim into the matching
  // inner-block; omitted on paths that don't preserve.
  unresolvedDeps?: Map<string, UnresolvedDepRef[]>,
): void {
  for (const [name, range] of Array.from(deps.entries()).sort((a, b) => cmpUtf16(a[0], b[0]))) {
    const resolution = resolveClassicEdgeTarget(diagnostics, srcId, name, range, specIndex, ladder)
    if (resolution.status === 'ambiguous') continue
    if (resolution.status === 'missing') {
      preserveClassicMissingDependency(diagnostics, srcId, name, range, kind, ladder, unresolvedDeps)
      continue
    }
    const attrs = classicResolvedEdgeAttrs(srcId, resolution.dstId, name, range, ladder)
    builder.addEdge(srcId, resolution.dstId, kind, attrs)
  }
}

// === SERIALIZE ==============================================================

// yarn 1 builds an entry-key line as `valKeys.sort(sortAlpha).map(maybeWrap)
// .join(', ')` (cli.js `_stringify`): each descriptor is quote-decided
// INDEPENDENTLY, then the (already-quoted-or-bare) descriptors are comma-joined.
// `sortAlpha` (yarn's `src/util/misc.js`) compares charCode-by-charCode and breaks
// an equal-prefix tie by the SHORTER string first — which JS string `<` (our
// `cmpUtf16`, applied by the caller `entrySpecsOfNode`) reproduces exactly (UTF-16
// code-unit order, shorter-prefix-first), so naming `sortAlpha` here is for
// precision, not a behavioural diff. There is NO whole-key quoting — a
// multi-descriptor key is `"@babel/core@^7.23.9", "@babel/core@^7.24.4":`
// (per-descriptor) or `acorn@^8.15.0, acorn@^8.16.0:` (all bare), NEVER the
// single-pair-wrapped `"a, b":` form. Whole-key quoting avoids the single-pair wrapper; it
// produced yarn-unreadable output for every dedup'd package.
export function stringifyEntryKey(specs: string[]): string {
  return specs
    .map(spec => (mustQuoteSpec(spec) ? `"${escapeQuoted(spec)}"` : spec))
    .join(', ')
}

// Per-descriptor quote decision — a faithful port of yarn 1's `shouldWrapKey`
// (@yarnpkg/lockfile). A descriptor is wrapped iff it begins with `true`/`false`,
// contains a colon / whitespace / backslash / `"` / `,` / `[` / `]`, or does not
// begin with an ASCII letter (the leading-`@` of a scoped descriptor, or a
// leading digit, falls here). Non-ASCII content is covered by the
// not-leading-letter clause for a leading non-ASCII char; a non-ASCII char later
// in an otherwise-bare descriptor is left bare exactly as yarn does (its rule
// inspects only the trigger class + the FIRST character).
//
// NB yarn's own `shouldWrapKey` carries a SEPARATE `/^[0-9]/` (leading-digit)
// clause alongside its not-a-letter check; that clause is REDUNDANT — a leading
// digit already fails `/^[a-zA-Z]/` — so it is intentionally folded into the
// single `!ENTRY_KEY_LEADING_ALPHA_RE.test(spec)` term here. One regex, identical
// outcome (a leading-digit descriptor like `7zip@^1` is quoted either way).
export function mustQuoteSpec(spec: string): boolean {
  return spec.startsWith('true')
    || spec.startsWith('false')
    || ENTRY_KEY_WRAP_RE.test(spec)
    || !ENTRY_KEY_LEADING_ALPHA_RE.test(spec)
}

export function quoteDepName(name: string): string {
  return name.startsWith('@') || !BARE_DEP_NAME_RE.test(name)
    ? `"${escapeQuoted(name)}"`
    : name
}

// ADR-0014 §4.F3: yarn-classic stringify round-trips any URL-shaped PM-native
// resolution — the same shapes `parseResolution` accepts (incl. git+ssh / git:
// / ssh: / scp-like `git@…`) — PLUS the relative `file:` / `link:` / `portal:`
// local specifiers. The local specifier is re-emitted verbatim, which
// preserves the file/link/portal distinction that a `directory` canonical would
// otherwise flatten to a single `file:` shape. PM-native strings from foreign
// sources (yarn-berry locators, pnpm workspace `link:` etc.) are neither shape
// → return undefined to delegate to the canonical-derived fallback instead of
// throwing.
export function formatResolution(input: string | undefined): string | undefined {
  if (input === undefined) return undefined
  return isYarnClassicResolvableUrl(input) || isYarnClassicLocalSpec(input) ? input : undefined
}

/** The registry base of a `<base>/<name>/-/<basename>-<ver>.tgz[#sha1]` URL (everything
 *  before the package path), or `undefined` for a non-registry-tarball shape. */
export function registryBaseOf(url: string): string | undefined {
  const m = REGISTRY_TARBALL_TAIL.exec(url)
  return m !== null ? url.slice(0, m.index) : undefined
}

/** A package's scope key for registry routing: `@mycorp` for `@mycorp/pkg`, `''` (default)
 *  for an unscoped name — mirrors yarn 1's `@scope:registry` config axis. */
export function scopeOf(name: string): string {
  return name.startsWith('@') ? name.slice(0, Math.max(0, name.indexOf('/'))) : ''
}

/** PER-SCOPE registry bases the lock ALREADY uses (majority vote over NATIVE
 *  `nativeResolution`-verbatim entries), so routing respects yarn 1's `@scope:registry`:
 *  a private-scoped package inherits its scope-siblings' registry, unscoped packages the
 *  default one — NOT a global override that would drag `@mycorp/*` onto the public
 *  registry. ONLY native entries count (a cross-format convert has none → falls back
 *  rather than learn npmjs from the source). `baseFor(name)` = the name's scope base, else
 *  the unscoped base, else yarn 1's default. */
export function inferRegistryBases(graph: Graph): (name: string) => string {
  const perScope = new Map<string, Map<string, number>>()   // scope → (base → count)
  for (const node of graph.nodes()) {
    const native = graph.tarballOf(node.id)?.nativeResolution
    const base = native !== undefined ? registryBaseOf(native) : undefined
    if (base === undefined) continue
    const scope = scopeOf(node.name)
    let counts = perScope.get(scope)
    if (counts === undefined) { counts = new Map(); perScope.set(scope, counts) }
    counts.set(base, (counts.get(base) ?? 0) + 1)
  }
  const majority = (counts: Map<string, number> | undefined): string | undefined => {
    let best: string | undefined; let bestN = 0
    for (const [b, n] of counts ?? []) if (n > bestN) { best = b; bestN = n }
    return best
  }
  const unscoped = majority(perScope.get(''))
  return (name: string): string =>
    majority(perScope.get(scopeOf(name))) ?? unscoped ?? DEFAULT_YARN_CLASSIC_REGISTRY
}

/** Canonical resolution after yarn-classic's target-aware registry projection.
 * Native entries never call this path: their verbatim `nativeResolution` wins.
 * Keeping this transform separate lets strict output comparison apply the exact
 * same host rewrite to newly minted graph payloads before comparing a reparse. */
export function projectCanonicalResolution(
  canonical: ResolutionCanonical | undefined,
  registryBase?: string,
): ResolutionCanonical | undefined {
  if (canonical?.type !== 'tarball' || registryBase === undefined) return canonical
  const m = REGISTRY_TARBALL_TAIL.exec(canonical.url)
  return m === null
    ? canonical
    : { ...canonical, url: registryBase + canonical.url.slice(m.index) }
}

/** Target-projected canonical resolutions keyed like `Graph.tarballs()`.
 * Only minted payloads participate; native entries are emitted verbatim and
 * therefore already compare against their original canonical URL. */
export function projectedCanonicalResolutions(
  graph: Graph,
  options: Pick<YarnClassicStringifyOptions, 'registryFor'> = {},
): ReadonlyMap<TarballKey, ResolutionCanonical> {
  const projected = new Map<TarballKey, ResolutionCanonical>()
  const inferBase = inferRegistryBases(graph)
  for (const node of graph.nodes()) {
    const payload = graph.tarballOf(node.id)
    if (payload?.nativeResolution !== undefined || payload?.resolution === undefined) continue
    const registryBase = options.registryFor?.(node.name) ?? inferBase(node.name)
    const resolution = projectCanonicalResolution(payload.resolution, registryBase)
    if (resolution !== payload.resolution && resolution !== undefined) {
      projected.set(toTarballKey(node), resolution)
    }
  }
  return projected
}

/** Integrity after a minted payload is serialized to classic's two carriers and
 * reparsed: SRI-emittable hashes acquire the neutral `sri` provenance. A
 * `url-fragment` sha1 is removed from the projected Integrity only when the
 * emitter actually carries it in `resolved`; otherwise it stays and makes the
 * strict snapshot expose the checksum drop. Unsupported origins stay for the
 * same fail-closed reason. */
export function projectedCanonicalIntegrities(
  graph: Graph,
  options: Pick<YarnClassicStringifyOptions, 'registryFor'> = {},
): ReadonlyMap<TarballKey, Integrity | undefined> {
  const projected = new Map<TarballKey, Integrity | undefined>()
  const inferBase = inferRegistryBases(graph)
  for (const node of graph.nodes()) {
    const payload = graph.tarballOf(node.id)
    if (payload?.nativeResolution !== undefined || payload?.integrity === undefined) continue
    const sha1Fragment = urlFragmentSha1(payload.integrity)
    const registryBase = options.registryFor?.(node.name) ?? inferBase(node.name)
    const resolved = deriveResolvedFromCanonical(
      payload.resolution,
      payload.integrity,
      registryBase,
    )
    const carriesSha1Fragment = sha1Fragment !== undefined
      && resolved?.includes(`#${sha1Fragment}`) === true
    const hashes = payload.integrity.hashes
      .filter(hash => !(carriesSha1Fragment
        && hash.algorithm === 'sha1'
        && hash.origin === 'url-fragment'
        && hash.digest === sha1Fragment))
      .map(hash => isTarballOrigin(hash.origin)
        ? { ...hash, origin: 'sri' as const }
        : hash)
    projected.set(toTarballKey(node), hashes.length === 0 ? undefined : { hashes })
  }
  return projected
}

export function deriveResolvedFromCanonical(
  canonical: ResolutionCanonical | undefined,
  integrity?: Integrity,
  registryBase?: string,
): string | undefined {
  if (canonical === undefined) return undefined
  // Rehost a registry tarball to the lock's registry base (see above); leave git / file /
  // non-`<registry>/<name>/-/…` tarballs untouched.
  const canon = projectCanonicalResolution(canonical, registryBase) ?? canonical
  // A synthesized tarball node carries the tarball sha1 as a `url-fragment` hash (from the
  // registry's `dist.shasum`); yarn-classic writes it as the `resolved#<sha1>` fragment,
  // matching yarn 1's convention so a bumped entry stays `--frozen-lockfile`-clean.
  // (Round-tripped nodes take the verbatim `nativeResolution` sidecar; this path is the
  // mint fallback.)
  const sha1Fragment = integrity !== undefined ? urlFragmentSha1(integrity) : undefined
  const candidate = stringifyForYarnClassic(canon, sha1Fragment !== undefined ? { sha1Fragment } : {})
  // `unknown` canonical returns its raw verbatim — for yarn-berry locators
  // (`<n>@patch:...`, `<n>@npm:<ver>`, etc.) this is NOT a re-parseable
  // yarn-classic `resolved` shape and the parse rejects on reparse. Filter the
  // emit to re-parseable shapes only: URLs PLUS the `file:` / `link:` /
  // `portal:` local specifiers (a `directory` canonical projects to
  // `file:./<path>`). The underlying loss is already attributed
  // by `reportPatchDrop` / `RECIPE_FEATURE_DROPPED` upstream when relevant.
  if (
    candidate !== undefined
    && !isYarnClassicResolvableUrl(candidate)
    && !isYarnClassicLocalSpec(candidate)
  ) {
    return undefined
  }
  return candidate
}

function createClassicStringifyContext(
  graph: Graph,
  options: YarnClassicStringifyOptions,
): ClassicStringifyContext {
  const inferBase = inferRegistryBases(graph)
  return {
    graph,
    options,
    emitDiagnostic: diagnostic => options.onDiagnostic?.(diagnostic),
    emitSidecar: sidecarByGraph.get(graph),
    warnedPeerEdges: new Set(),
    warnedPeerContexts: new Set(),
    warnedPatches: new Set(),
    registryBaseFor: name => options.registryFor?.(name) ?? inferBase(name),
  }
}

// Collapse identities classic cannot represent while retaining source-forked
// siblings and preferring the unpatched representative.
function dedupeClassicNodes(context: ClassicStringifyContext): Node[] {
  const sorted = Array.from(context.graph.nodes()).sort((a, b) =>
    cmpUtf16(a.id, b.id)
      || ((a.patch === undefined ? 0 : 1) - (b.patch === undefined ? 0 : 1)),
  )
  const identities = new Set<string>()
  const nodes: Node[] = []
  for (const node of sorted) {
    if (node.workspacePath !== undefined) {
      nodes.push(node)
      continue
    }
    const identity = node.source === undefined
      ? `${node.name}@${node.version}`
      : `${node.name}@${node.version}+src=${node.source}`
    if (identities.has(identity)) {
      reportPatchDrop(node, context.warnedPatches, context.emitDiagnostic)
      continue
    }
    identities.add(identity)
    nodes.push(node)
  }
  return nodes
}

function buildClassicEntry(
  context: ClassicStringifyContext,
  node: Node,
): { key: string; text: string; entrySortKey: string } {
  reportPatchDrop(node, context.warnedPatches, context.emitDiagnostic)
  reportPeerContextFlatten(node, context.warnedPeerContexts, context.emitDiagnostic)
  reportDroppedPeerEdges(context.graph, node.id, context.warnedPeerEdges, context.emitDiagnostic)
  const specs = entrySpecsOfNode(context.graph, node)
  const key = stringifyEntryKey(specs)
  const lines = [`${key}:`, `  version "${escapeQuoted(node.version)}"`]
  appendClassicResolutionFields(context, node, lines)
  appendClassicEntryExtras(context, node, lines)
  appendClassicDependencyBlocks(context, node, lines)
  return { key, text: lines.join('\n'), entrySortKey: specs[0] ?? key }
}

function appendClassicResolutionFields(
  context: ClassicStringifyContext,
  node: Node,
  lines: string[],
): void {
  const payload = context.graph.tarballOf(node.id)
  const resolved = formatResolution(payload?.nativeResolution)
    ?? deriveResolvedFromCanonical(payload?.resolution, payload?.integrity, context.registryBaseFor(node.name))
  if (resolved !== undefined) lines.push(`  resolved "${escapeQuoted(resolved)}"`)
  const integrity = payload?.integrity && emitSri(payload.integrity)
  if (integrity !== undefined && integrity !== '') {
    lines.push(`  integrity ${integrity.includes(' ') ? `"${integrity}"` : integrity}`)
  }
}

function appendClassicEntryExtras(
  context: ClassicStringifyContext,
  node: Node,
  lines: string[],
): void {
  const extras = context.emitSidecar?.entryExtras?.get(node.id)
  if (extras === undefined) return
  for (const raw of extras) lines.push(`  ${raw}`)
}

function appendClassicDependencyBlocks(
  context: ClassicStringifyContext,
  node: Node,
  lines: string[],
): void {
  const dependencies = withUnresolvedDepRefs(
    collectDependencyBlockEntries(context.graph, node.id, context.emitDiagnostic),
    unresolvedDepRefsOfNode(context.emitSidecar, node.id, 'dependencies'),
  )
  appendClassicDependencyBlock(lines, 'dependencies', dependencies)
  const optionalDependencies = withUnresolvedDepRefs(
    collectBlockEntries(context.graph, context.graph.out(node.id, 'optional'), context.emitDiagnostic),
    unresolvedDepRefsOfNode(context.emitSidecar, node.id, 'optionalDependencies'),
  )
  appendClassicDependencyBlock(lines, 'optionalDependencies', optionalDependencies)
}

function appendClassicDependencyBlock(
  lines: string[],
  label: 'dependencies' | 'optionalDependencies',
  entries: readonly (readonly [string, string])[],
): void {
  if (entries.length === 0) return
  lines.push(`  ${label}:`)
  for (const [name, range] of entries) {
    lines.push(`    ${quoteDepName(name)} ${mustQuoteSpec(range) ? `"${escapeQuoted(range)}"` : range}`)
  }
}

function satisfiesSafe(version: string, range: string): boolean {
  try { return semver.satisfies(version, range) } catch { return false }
}

