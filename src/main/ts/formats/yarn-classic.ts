const HEADER =
  '# THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.\n' +
  '# yarn lockfile v1\n\n\n'
const HEADER_LINE_1 = '# THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.'
const HEADER_LINE_2 = '# yarn lockfile v1'
const cmpUtf16 = (a: string, b: string): number => a < b ? -1 : a > b ? 1 : 0

// A dependency pair `<name> <value>` where each side is a quoted string OR a
// bare whitespace-free token. Yarn 1 emits bare ranges/tags/`v`-prefixed
// versions (`latest`, `v0.5.0`, `^1.0.0`) unquoted — the value side must accept
// them, not only quoted strings.
const DEP_PAIR_RE = /^("(?:\\.|[^"])*"|[^\s"]\S*) ("(?:\\.|[^"])*"|[^\s"]\S*)$/
const BARE_DEP_NAME_RE = /^[A-Za-z0-9._-]+$/
// The EXACT trigger class from yarn 1's `shouldWrapKey` (@yarnpkg/lockfile
// stringify.js, mirrored in yarn cli.js): a descriptor is wrapped in quotes iff
// it contains a colon, ANY whitespace, a backslash, a double-quote, a comma, or
// a square bracket — i.e. `/[:\s\\",[\]]/`. This is DELIBERATELY narrower than a
// "quote anything non-alphanumeric" heuristic: yarn leaves `^`, `~`, `>`, `<`,
// `=`, `*`, `|`, `#`, `?`, `&`, `!`, `'`, backtick, and `{}` BARE inside a range
// (e.g. `amdefine@>=0.0.4:`, `acorn@^8.5.0:`, a bare `foo@*:`). Over-quoting
// those diverges from real yarn byte-for-byte (a corpus-confirmed regression).
const ENTRY_KEY_WRAP_RE = /[:\s\\",[\]]/
// yarn's `shouldWrapKey` ALSO wraps any descriptor that does not begin with an
// ASCII letter — this is what forces `@scope/...` descriptors (and any leading
// digit) to be quoted, independent of the trigger class above.
const ENTRY_KEY_LEADING_ALPHA_RE = /^[a-zA-Z]/
const BERRY_HEADER_RE = /^__metadata:\s*(?:\r?\n|$)/m

// F8 — a per-node verbatim reference to a dependency whose TARGET is ABSENT from
// the lock entirely (a genuine Rung-4 drop: no entry, no candidate, never an
// ambiguous miss). yarn-classic joins consumer ranges to entries by exact
// `<name>@<range>` key; when the target package has NO entry block at all there
// is nothing to bind, so we do NOT mint a phantom node (identity stays clean) —
// instead we capture the dependency line VERBATIM (its on-disk block kind,
// dep-name, exact range string) and re-emit it into the same inner-block on
// stringify, so a SAME-FORMAT round-trip is byte-faithful. Mirrors the
// yarn-berry `UnresolvedDepRef` (#103) one-for-one. The MISSING-ENTRY diagnostic
// still fires alongside capture — preservation keeps BOTH the bytes and the
// signal; it never silences the warning. Same-format round-trip fidelity only:
// never promoted to a graph edge, never carried across a cross-PM convert (the
// sidecar is classic-local).
interface UnresolvedDepRef {
  block: 'dependencies' | 'optionalDependencies'
  name: string
  range: string
}

interface YarnClassicSidecar {
  entrySpecs: Map<string, string[]>
  /** Per-NodeId verbatim unknown entry-field lines (see YarnClassicEntry.extras),
   *  for round-trip re-emit. */
  entryExtras?: Map<string, string[]>
  /** F8 — per-node verbatim refs to dep targets ABSENT from the lock (genuine
   *  Rung-4 drops). Same-format round-trip fidelity only (see UnresolvedDepRef). */
  unresolvedDeps?: Map<string, UnresolvedDepRef[]>
}

interface YarnClassicEntry {
  key: string
  version?: string
  resolved?: string
  integrity?: string
  /** Unknown scalar entry fields captured VERBATIM (sans the 2-space indent) —
   *  e.g. yarn-1's legacy `uid ""` on link:/file: entries. Tolerated +
   *  round-tripped rather than rejected (forward-compat for unmodelled fields). */
  extras?: string[]
  dependencies: Map<string, string>
  optionalDependencies: Map<string, string>
}

