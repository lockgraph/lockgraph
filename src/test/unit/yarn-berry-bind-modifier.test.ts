import { describe, expect, it } from 'vitest'
import {
  parse as parseResolution,
  sourceDiscriminatorOf,
  type ResolutionCanonical,
} from '../../main/ts/recipe/resolution.ts'
import { detect, parse, stringify } from '../../main/ts/index.ts'

// Regression for the published-.62 parse crash: two yarn-berry entries for the
// SAME `name@version` differing ONLY by a `::` bind modifier collapsed onto one
// NodeId and threw IRREDUCIBLE_LOSS. The bind was silently dropped from identity
// (and, for `__archiveUrl=`, swept into the derived registry URL while leaving
// the host as registry.npmjs.org). ADR-0032 §"+src=" extension: the bind forks
// the NodeId (like `+patch=`); `__archiveUrl=` forks via its non-registry host.

const CK = (c: string) => `10c0/${c.repeat(128)}`

const ARCHIVE_LOCK = `# This file is generated by running "yarn install" inside your project.
# Manual changes might be lost - proceed with caution!

__metadata:
  version: 8
  cacheKey: 10c0

"string-width@npm:^4.1.0":
  version: 4.2.3
  resolution: "string-width@npm:4.2.3::__archiveUrl=https%3A%2F%2Fnpm.corp.example.com%2Fstring-width%2F-%2Fstring-width-4.2.3.tgz"
  checksum: ${CK('b')}
  languageName: node
  linkType: hard

"string-width@npm:^4.2.0":
  version: 4.2.3
  resolution: "string-width@npm:4.2.3"
  checksum: ${CK('a')}
  languageName: node
  linkType: hard
`

describe('recipe/resolution — yarn-berry `::` bind modifier (ADR-0032 +src= extension)', () => {
  it('`::__archiveUrl=<enc>` → tarball canonical on the DECODED archive url, full bind suffix carried for identity', () => {
    const c = parseResolution(
      'string-width@npm:4.2.3::__archiveUrl=https%3A%2F%2Fnpm.corp.example.com%2Fstring-width%2F-%2Fstring-width-4.2.3.tgz',
      { sourceKind: 'yarn-berry-locator', name: 'string-width' },
    )
    expect(c).toEqual({
      type: 'tarball',
      // the archive IS the fetch source — a non-registry host
      url: 'https://npm.corp.example.com/string-width/-/string-width-4.2.3.tgz',
      // the FULL bind suffix rides identity so a residual `&locator=`/`&hash=`
      // qualifier, an archive pinned to the default-registry host, or two
      // archives on the same host but different paths all fork the NodeId
      bind: '__archiveUrl=https%3A%2F%2Fnpm.corp.example.com%2Fstring-width%2F-%2Fstring-width-4.2.3.tgz',
    })
  })

  it('a plain `::version=` bind → registry url (clean version) + `bind` field', () => {
    const c = parseResolution(
      'string-width@npm:4.2.3::version=4.2.3&hash=abcdef',
      { sourceKind: 'yarn-berry-locator', name: 'string-width' },
    )
    expect(c).toEqual({
      type: 'tarball',
      url: 'https://registry.npmjs.org/string-width/-/string-width-4.2.3.tgz',
      bind: 'version=4.2.3&hash=abcdef',
    })
  })

  it('no bind → unchanged bare registry tarball (the 99% path)', () => {
    const c = parseResolution('string-width@npm:4.2.3', {
      sourceKind: 'yarn-berry-locator',
      name: 'string-width',
    })
    expect(c).toEqual({
      type: 'tarball',
      url: 'https://registry.npmjs.org/string-width/-/string-width-4.2.3.tgz',
    })
  })

  it('source discriminator: registry tarball is BARE; archive-url and bind-bearing tarballs FORK', () => {
    const bare: ResolutionCanonical = {
      type: 'tarball',
      url: 'https://registry.npmjs.org/string-width/-/string-width-4.2.3.tgz',
    }
    const archive: ResolutionCanonical = {
      type: 'tarball',
      url: 'https://npm.corp.example.com/string-width/-/string-width-4.2.3.tgz',
    }
    const bound: ResolutionCanonical = {
      type: 'tarball',
      url: 'https://registry.npmjs.org/string-width/-/string-width-4.2.3.tgz',
      bind: 'version=4.2.3',
    }
    expect(sourceDiscriminatorOf(bare)).toBeUndefined()
    expect(sourceDiscriminatorOf(archive)).toMatch(/^[0-9a-f]{16}$/)
    expect(sourceDiscriminatorOf(bound)).toMatch(/^[0-9a-f]{16}$/)
    // a registry tarball WITH a bind forks AWAY from the same-host bare one
    expect(sourceDiscriminatorOf(bound)).not.toBe(sourceDiscriminatorOf(bare))
    // and the two non-bare classes do not collide with each other
    expect(sourceDiscriminatorOf(bound)).not.toBe(sourceDiscriminatorOf(archive))
  })

  it('two bind-bearing entries that differ ONLY by the bind value fork distinct sources', () => {
    const a = parseResolution('pkg@npm:1.0.0::version=1.0.0&hash=aaaa', {
      sourceKind: 'yarn-berry-locator',
      name: 'pkg',
    })
    const b = parseResolution('pkg@npm:1.0.0::version=1.0.0&hash=bbbb', {
      sourceKind: 'yarn-berry-locator',
      name: 'pkg',
    })
    expect(sourceDiscriminatorOf(a)).not.toBe(sourceDiscriminatorOf(b))
  })
})

describe('yarn-berry — `::` bind modifier parse + round-trip (full pipeline)', () => {
  it('the repro: two `__archiveUrl=` entries parse to TWO distinct nodes (no throw), distinct +src/integrity', () => {
    const fmt = detect(ARCHIVE_LOCK)
    expect(fmt).toBe('yarn-berry-v8')
    const g = parse(fmt!, ARCHIVE_LOCK)
    const nodes = Array.from(g.nodes())
    expect(nodes).toHaveLength(2)

    const ids = nodes.map(n => n.id).sort()
    expect(ids).toEqual([
      'string-width@4.2.3',
      'string-width@4.2.3+src=839a6555cde138e4',
    ])

    // distinct sources: the registry entry is bare, the archive entry forks
    const bare = nodes.find(n => n.id === 'string-width@4.2.3')!
    const forked = nodes.find(n => n.id.includes('+src='))!
    expect((bare as { source?: string }).source).toBeUndefined()
    expect((forked as { source?: string }).source).toBe('839a6555cde138e4')

    // distinct integrity preserved per node (aaa… vs bbb…)
    const bareDigest = g.tarballOf(bare.id)?.integrity?.hashes?.[0]?.digest
    const forkedDigest = g.tarballOf(forked.id)?.integrity?.hashes?.[0]?.digest
    expect(bareDigest).toMatch(/^a+$/)
    expect(forkedDigest).toMatch(/^b+$/)
    expect(bareDigest).not.toBe(forkedDigest)
  })

  it('round-trip: parse→stringify→parse is byte-stable (canonical form) + identity-preserving', () => {
    const fmt = detect(ARCHIVE_LOCK)!
    const g = parse(fmt, ARCHIVE_LOCK)
    const ids1 = Array.from(g.nodes()).map(n => n.id).sort()

    const out = stringify(fmt, g)
    // the archive locator survives verbatim through nativeResolution
    expect(out).toContain(
      'resolution: "string-width@npm:4.2.3::__archiveUrl=https%3A%2F%2Fnpm.corp.example.com%2Fstring-width%2F-%2Fstring-width-4.2.3.tgz"',
    )

    const g2 = parse(fmt, out)
    const ids2 = Array.from(g2.nodes()).map(n => n.id).sort()
    expect(ids2).toEqual(ids1)

    // emit is a byte-stable fixed point (re-emit of the re-parsed graph)
    expect(stringify(fmt, g2)).toBe(out)
  })

  it('alias amplifier: an npm-aliased entry resolving to a `__archiveUrl=` locator does not collide', () => {
    // `string-width-cjs@npm:string-width@^4.2.0` (the @isaacs/cliui trio shape):
    // the alias collapses onto the target name `string-width`, and the archive
    // entry must still fork rather than collide with the plain registry entry.
    const lock = `__metadata:
  version: 8
  cacheKey: 10c0

"string-width@npm:^4.2.0":
  version: 4.2.3
  resolution: "string-width@npm:4.2.3"
  checksum: ${CK('a')}
  languageName: node
  linkType: hard

"string-width-cjs@npm:string-width@^4.2.0":
  version: 4.2.3
  resolution: "string-width@npm:4.2.3::__archiveUrl=https%3A%2F%2Fnpm.corp.example.com%2Fstring-width%2F-%2Fstring-width-4.2.3.tgz"
  checksum: ${CK('c')}
  languageName: node
  linkType: hard
`
    const fmt = detect(lock)!
    const g = parse(fmt, lock)
    const ids = Array.from(g.nodes()).map(n => n.id).sort()
    expect(ids).toEqual([
      'string-width@4.2.3',
      'string-width@4.2.3+src=839a6555cde138e4',
    ])
  })

  it('a `::version=`/`::hash=` variant: two entries differing only by the bind → 2 distinct nodes (bind field path)', () => {
    const lock = `__metadata:
  version: 8
  cacheKey: 10c0

"pkg@npm:^1.0.0":
  version: 1.0.0
  resolution: "pkg@npm:1.0.0::version=1.0.0&hash=aaaa"
  checksum: ${CK('a')}
  languageName: node
  linkType: hard

"pkg@npm:^1":
  version: 1.0.0
  resolution: "pkg@npm:1.0.0::version=1.0.0&hash=bbbb"
  checksum: ${CK('b')}
  languageName: node
  linkType: hard
`
    const fmt = detect(lock)!
    const g = parse(fmt, lock)
    const nodes = Array.from(g.nodes())
    expect(nodes).toHaveLength(2)
    // both forked (registry host but distinct binds), distinct +src
    const sources = nodes.map(n => (n as { source?: string }).source)
    expect(sources.every(s => s !== undefined)).toBe(true)
    expect(sources[0]).not.toBe(sources[1])

    // round-trips through the lockgraph F-section (resolution.bind flattens)
    const lgRaw = stringify('lockgraph', g)
    const g2 = parse('lockgraph', lgRaw)
    const ids2 = Array.from(g2.nodes()).map(n => n.id).sort()
    expect(ids2).toEqual(nodes.map(n => n.id).sort())
  })

  it('two `__archiveUrl=` entries differing ONLY by a trailing `&locator=` qualifier fork (no IRREDUCIBLE_LOSS)', () => {
    // Enterprise mirror: same archive host+path, distinct yarn qualifier. The
    // residual `&locator=` past the `__archiveUrl=` pair must discriminate, else
    // both canonicalise identically and collapse onto one NodeId.
    const enc = 'https%3A%2F%2Fnexus.corp%2Fdual%2F-%2Fdual-1.0.0.tgz'
    const lock = `__metadata:
  version: 8
  cacheKey: 10c0

"dual@npm:^1.0.0":
  version: 1.0.0
  resolution: "dual@npm:1.0.0::__archiveUrl=${enc}&locator=foo"
  checksum: ${CK('a')}
  languageName: node
  linkType: hard

"dual@npm:^1":
  version: 1.0.0
  resolution: "dual@npm:1.0.0::__archiveUrl=${enc}&locator=bar"
  checksum: ${CK('b')}
  languageName: node
  linkType: hard
`
    const fmt = detect(lock)!
    const g = parse(fmt, lock) // must NOT throw IRREDUCIBLE_LOSS
    const nodes = Array.from(g.nodes())
    expect(nodes).toHaveLength(2)
    const sources = nodes.map(n => (n as { source?: string }).source)
    expect(sources.every(s => s !== undefined)).toBe(true)
    expect(sources[0]).not.toBe(sources[1])
  })

  it('an `__archiveUrl=` pin to the DEFAULT registry host forks from a bare un-pinned entry (no IRREDUCIBLE_LOSS)', () => {
    // Offline-mirror configs rewrite EVERY entry with `__archiveUrl`, including
    // ones that point back at the default registry. The explicit pin is itself a
    // discriminating fact: it must fork from a sibling plain registry entry
    // rather than collapse onto the same `+src`-bare NodeId.
    const enc = 'https%3A%2F%2Fregistry.npmjs.org%2Freg%2F-%2Freg-1.0.0.tgz'
    const lock = `__metadata:
  version: 8
  cacheKey: 10c0

"reg@npm:^1.0.0":
  version: 1.0.0
  resolution: "reg@npm:1.0.0"
  checksum: ${CK('a')}
  languageName: node
  linkType: hard

"reg@npm:^1":
  version: 1.0.0
  resolution: "reg@npm:1.0.0::__archiveUrl=${enc}"
  checksum: ${CK('b')}
  languageName: node
  linkType: hard
`
    const fmt = detect(lock)!
    const g = parse(fmt, lock) // must NOT throw
    const nodes = Array.from(g.nodes())
    expect(nodes).toHaveLength(2)
    const bare = nodes.find(n => (n as { source?: string }).source === undefined)
    const forked = nodes.find(n => (n as { source?: string }).source !== undefined)
    expect(bare).toBeDefined()
    expect(forked).toBeDefined()
  })
})
